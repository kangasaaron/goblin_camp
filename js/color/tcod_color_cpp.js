/**
@PageName color
@FuncTitle Shift a color's hue up or down
@FuncDesc The hue shift value is the number of grades the color's hue will be shifted. The value can be negative for shift left, or positive for shift right.
	Resulting values H < 0 and H >= 360 are handled automatically.
@Cpp void TCODColor::shiftHue (float hshift)
@C void TCOD_color_shift_hue (TCOD_color_t *c, float hshift)
@C# TCODColor::shiftHue(float hshift)
@Lua Color:shiftHue(hshift)
@Param c	The color to modify
@Param hshift	The hue shift value

 *  @brief Shift a colors hue by an amount.
 *
 *  @param {TCOD_color_t} color Pointer to a color struct.
 *  @param {number} hue_shift The distance to shift the hue, in degrees.
 */

TCODColor.prototype.shiftHue = function shiftHue(h_shift) {
    let c = { r: this.r, g: this.g, b: this.b };
    if (h_shift !== 0) {
        TCOD_color_set_HSV(
            c, TCOD_color_get_hue(c) + hue_shift, TCOD_color_get_saturation(c), TCOD_color_get_value(c));
    }
    this.r = c.r;
    this.g = c.g;
    this.b = c.b;
}

/**
 * @PageName color
 * @FuncTitle Scale a color's saturation and value
 *
 *  @param {number} saturation_coef Multiplier for this colors saturation.
 *  @param {number} value_coef Multiplier for this colors value.
 */
TCODColor.prototype.scaleHSV = function scaleHSV(s_scale, v_scale) {
    let c = { r: this.r, g: this.g, b: this.b };
    TCOD_color_set_HSV(
        c,
        TCOD_color_get_hue(c),
        TCOD_color_get_saturation(c) * s_scale,
        TCOD_color_get_value(c) * v_scale);
    this.r = c.r;
    this.g = c.g;
    this.b = c.b;
}

