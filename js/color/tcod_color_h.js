/**
 *  A three channel color struct.
 */

export class TCOD_ColorRGB {
    r = 0;
    g = 0;
    b = 0;
    constructor(r, g, b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }
    equals(rhs) {
        return this.r == rhs.r &&
            this.g == rhs.g &&
            this.b == rhs.b;
    }
    isNotEqualTo(rhs) {
        return !this.equals(rhs);
    }
}
export const TCOD_color_t = TCOD_ColorRGB;
/**
 *  A four channel color struct.
 */
export class TCOD_ColorRGBA {
    r = 0;
    g = 0;
    b = 0;
    a = 1;
    constructor(r, g, b, a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
    equals(rhs) {
        return (
            this.r == rhs.r && this.g == rhs.g && this.b == rhs.b && this.a == rhs.a
        );
    }
    isNotEqualTo(rhs) {
        return !this.equals(rhs);
    }
};


